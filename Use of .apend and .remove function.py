#to add specific element in list by append method
lst=[1,2,3,4]
lst.append(15)
print(lst)

#to remove specific element in list by remove method
lst=[1,2,3,4]
lst.remove(4)
print(lst)

#to add specific element at specific index in list by insert method
lst=[1,2,3,4]
lst.insert(2,'15')
print(lst)